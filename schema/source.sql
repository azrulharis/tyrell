資本治安部隊が最初に侵略者に圧倒された後、1月31日まで続くブラジリアへの連邦治安介入を発表したダ・シルバは、ボルソナロが不正選挙の可能性に関する根拠のない主張のキャンペーンの後、彼の支持者を扇動したと非難した。

大統領の同盟国はまた、首都ブラジリアの公安部隊がいかに準備不足であり、週末のデモのために集まるために何日もソーシャルメディアで計画していた暴徒に簡単に圧倒されたことについて疑問を投げかけた.




