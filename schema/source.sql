ブラジリア – ブラジルの極右元大統領ジャイル・ボルソナロの支持者は、日曜日に国の議会、大統領官邸、最高裁判所に侵入し、汚損した.

彼らの大暴れによる死傷者の即時の報告はありませんでしたが、侵略者は破壊の痕跡を残し、大統領官邸の破壊された窓から家具を投げつけ、議会の一部をスプリンクラーシステムであふれさせ、最高裁判所の式典室を略奪しました.

首都で暴動を起こしている黄色と緑の服を着た何千人もの抗議者の光景は、10月30日の大統領投票に続く緊張の数ヶ月を締めくくった。

10月の選挙でボルソナロを破った左派のルイス・イナシオ・ルーラ・ダ・シルバ大統領が就任してから数日後、3時間強続いた蜂起は、国を依然として支配している深刻な二極化を強調した.

